<h3>LONG DISTANCE DELIVERY PROGRAM</h3>

<p>We partner with an outside, professional delivery company to deliver to your home. </p>

<p>Delivery available anywhere in U.S., outside of our local delivery zones. </p>

<p>You could choose from the following:</p>


<p><strong>Curbside delivery:</strong> This is recommended for small or medium sized boxed furniture pieces like chairs, end tables, floor lamps, mirrors, etc. The items would be dropped off at your home. The customer is responsible for moving the item into the home. The customer is responsible for disposing of packing materials. Estimated delivery time would be 10-14 working business days from the day the order leaves our warehouse. The carrier company would contact you before delivery.</p>

<p><strong>White Glove delivery:</strong> This is recommended for larger orders and large furniture pieces like sofa, bed, dining set, armoire, etc. The delivery team would place the items inside the home and assemble them. All packing materials would be removed. Estimated delivery time would be 7-10 working business days from the day the order leaves our warehouse. The carrier company would contact you before delivery.</p>

<h3>UPS or FedEX Shipments</h3>

<p>UPS or FedEX is recommended for small accessories and soft goods. We ship anywhere in the United States. Estimated delivery time would be 7-10 working business days from the day the order leaves our warehouse. The delivery cost would be calculated when you place the order.</p>


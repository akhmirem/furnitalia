<h3 dir="ltr" ><a name="7"></a>What is your long distance delivery program?</h3>

<p>We partner with an outside, professional delivery company to deliver to your home. Delivery available anywhere in U.S., outside of our local delivery zones. You could choose either one :</p>

<p>Curbside delivery : This is recommended for small or medium sized boxed furniture pieces like chairs, end tables, floor lamps, mirrors, etc. The items would be dropped off at your home. The customer is responsible for moving the item into the home. The customer is responsible for disposing of packing materials. Estimated delivery time would be 10-14 working business days from the day the order leaves our warehouse. The carrier company would contact you before delivery.</p>

<p>White Glove delivery : This is recommended for larger orders and large furniture pieces like sofa, bed, dining set, armoire, etc. The delivery team would place the items inside the home and assemble them. All packing materials would be removed. Estimated delivery time would be 7-10 working business days from the day the order leaves our warehouse. The carrier company would contact you before delivery.</p>

